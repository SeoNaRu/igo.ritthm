# 음 뭐지?
# 일단 펙토리얼이 뭔지 정확하게 이해해야겠다
# 정의 보니까 n! = n x (n - 1) x (n -2) ... x 2 x 1  

# 반복문 돌려서 하면되겠네
# 그리고 다 곱한다음에 하나씩 자르고 0포함되면 카운트 +1 이런식
# 일단은 하나씩 곱하는거 부터 로그 찍어봐야징 ㅋ

N = int(input())
count = 0
total = 1
i = 1
for i in range(1,N+1):
    total *= i
    
