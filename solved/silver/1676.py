# 아하 ㅋ 샤갈 그냥 다른거였네 레전드 함정
# 앞에 0이 나오기전 뒤에 나오는 0만 찾는거구나

# 001100 이런식이면 2개라는 말이지
# 리버스 하고 0찾고 count가 1이상이고 0이 아니면 바로 멈춰 해버려야겠다 ㅋ 이제 진짜 쉽노
for char in reversed(str(total)):
    if char == '0':
        count += 1
    if char != '0' and count > 1:
        break;

print(count);

